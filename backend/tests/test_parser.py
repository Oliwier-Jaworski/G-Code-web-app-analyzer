"""Unit tests for the G-code parser.

Each test feeds a small in-memory G-code string and asserts on specific
fields of the analyze() result. The intent is to lock down the current
behaviour before further changes (slicer headers, arcs, acceleration).
"""

from __future__ import annotations

import math

import pytest

from parser import analyze


def _run(src: str, **kwargs) -> dict:
    # Default to the naive (no-acceleration) time model for stable, exact
    # arithmetic in the existing time/speed tests. Tests that exercise the
    # acceleration model pass accel_mm_s2 explicitly.
    kwargs.setdefault("accel_mm_s2", None)
    return analyze(src.splitlines(), **kwargs)


def test_basic_xyz_bounds():
    src = """
    G90
    M82
    G1 X0 Y0 Z0.2 E0
    G1 X10 Y0 E1
    G1 X10 Y10 E2
    G1 X0 Y10 E3
    G1 X0 Y0 E4
    """
    r = _run(src)
    assert r["bounds"] == {"x": [0.0, 10.0], "y": [0.0, 10.0], "z": [0.2, 0.2]}
    assert r["size"] == {"x": 10.0, "y": 10.0, "z": 0.0}
    assert r["lineCount"] == len(src.splitlines())


def test_relative_xyz_g91():
    # G91: every X10 adds 10 to current x.
    src = """
    G91
    M82
    G1 X10 Y0 Z0.2 E1
    G1 X10 Y0 E2
    G1 X10 Y0 E3
    """
    r = _run(src)
    # First extrusion segment seeds bounds with the start point (0,0,0),
    # so x range is [0, 30] after three +10 hops.
    assert r["bounds"]["x"] == [0.0, 30.0]


