def test_print_bounds_excludes_homing():
    """Homing/probing moves to negative coordinates must NOT pollute bounds."""
    src = """
    G90
    M82
    G28
    G1 X-100 Y-100 Z5
    G1 X-100 Y0
    G1 X0 Y0 Z0.2 E0
    G1 X10 Y0 E1
    G1 X10 Y10 E2
    G1 X0 Y10 E3
    G1 X0 Y0 E4
    """
    r = _run(src)
    assert r["bounds"]["x"] == [0.0, 10.0]
    assert r["bounds"]["y"] == [0.0, 10.0]
    assert r["motionBounds"]["x"][0] == -100.0
    assert r["motionBounds"]["y"][0] == -100.0


