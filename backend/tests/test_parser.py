def test_toolpath_layer_offsets_from_comments():
    src = """
G90
M82
;LAYER:0
G1 X0 Y0 Z0.2
G1 X10 Y0 E1
;LAYER:1
G1 X10 Y10 Z0.4 E2
G1 X0 Y10 E3
"""
    r = analyze(src.splitlines(), collect_toolpath=True, accel_mm_s2=None)
    tp = r["toolpath"]
    # Layer 0: 1 travel (Z lift) + 1 extrusion. Layer 1: 2 extrusions.
    assert tp["layers"] == 2
    assert tp["extrusionLayerOffsets"] == [0, 1, 3]
    assert tp["travelLayerOffsets"] == [0, 1, 1]


def test_toolpath_layer_offsets_fallback_z():
    # No ;LAYER: comments: a new extrusion Z starts a new layer.
    src = """
G90
M82
G1 X0 Y0 Z0.2
G1 X10 Y0 E1
G1 X10 Y0 Z0.4
G1 X0 Y0 E2
"""
    r = analyze(src.splitlines(), collect_toolpath=True, accel_mm_s2=None)
    tp = r["toolpath"]
    assert tp["layers"] == 2
    assert tp["extrusionLayerOffsets"] == [0, 1, 2]
    # Initial Z lift and the Z hop between layers are both travel in layer 0
    # and layer 0 respectively (the hop happens before layer 1's extrusion).
    assert tp["travelLayerOffsets"] == [0, 2, 2]


def test_toolpath_layer_offsets_survive_decimation():
    # 40 layers x 5 extrusion segments, decimated to <= 20 segments.
    lines = ["G90", "M82"]
    for layer in range(40):
        lines.append(f";LAYER:{layer}")
        z = 0.2 * (layer + 1)
        lines.append(f"G1 X0 Y{layer} Z{z:.2f}")
        for i in range(5):
            lines.append(f"G1 X{(i + 1) * 10} Y{layer} E{layer * 5 + i + 1}")
    r = analyze(lines, collect_toolpath=True, accel_mm_s2=None,
                toolpath_max_segments=20)
    tp = r["toolpath"]
    ext = tp["extrusionLayerOffsets"]
    trav = tp["travelLayerOffsets"]
    assert tp["layers"] == 40
    assert len(ext) == 41 and len(trav) == 41
    # Offsets must be monotonic and account for every sampled segment.
    assert all(a <= b for a, b in zip(ext, ext[1:]))
    assert all(a <= b for a, b in zip(trav, trav[1:]))
    assert ext[-1] == len(tp["extrusion"]) // 6
    assert trav[-1] == len(tp["travel"]) // 6
    assert ext[-1] + trav[-1] == tp["sampledSegments"]


