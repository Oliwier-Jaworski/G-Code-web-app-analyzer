def test_arc_quarter_circle_g2():
    # Start at (10, 0). Center at (0, 0) via I=-10 J=0. End at (0, 10).
    # Quarter circle CCW would be G3; G2 from (10,0) to (0,10) clockwise
    # actually goes the "long way" (3/4 turn). Use G3 for a quarter (CCW).
    src = """
    G90
    M82
    G1 F6000
    G1 X10 Y0 Z0.2 E0
    G3 X0 Y10 I-10 J0 E1
    """
    r = _run(src)
    # Print distance for the arc only = quarter circle of radius 10 = pi/2 * 10.
    expected = math.pi / 2 * 10
    # printDistance sums all extrusion moves; first move is travel only? No,
    # G1 X10 Y0 has E0 which equals current E (0), so de=0, not extrusion.
    assert r["printDistanceMm"] == pytest.approx(expected, rel=1e-6)


def test_arc_full_circle_g3():
    # From (10, 0) back to (10, 0) with center (0,0) via I=-10 J=0 = full
    # circle (sweep = 2*pi). Length = 2*pi*r = 2*pi*10.
    src = """
    G90
    M82
    G1 F6000
    G1 X10 Y0 Z0.2 E0
    G3 X10 Y0 I-10 J0 E1
    """
    r = _run(src)
    expected = 2 * math.pi * 10
    assert r["printDistanceMm"] == pytest.approx(expected, rel=1e-6)


def test_acceleration_short_segment():
    # Segment too short to reach feed-rate speed.
    # F=6000 -> v_target = 100 mm/s. Accel = 1500.
    # d_accel = v^2 / (2a) = 10000/3000 = 3.333 mm.
    # 2 * d_accel = 6.667 mm; dist=2 mm < 6.667 -> triangle profile.
    # t = 2 * sqrt(dist / a) = 2 * sqrt(2/1500) = ~0.0730s.
    src = """
    G90
    M82
    G1 F6000
    G1 X2 Y0 E1
    """
    r = analyze(src.splitlines(), accel_mm_s2=1500.0)
    expected = 2 * math.sqrt(2.0 / 1500.0)
    assert r["estimatedTimeS"] == pytest.approx(expected, rel=1e-6)


def test_acceleration_long_segment_trapezoid():
    # Long segment: v_target=100, a=1500, dist=100.
    # d_accel = 3.333; trapezoid time = 2*v/a + (dist - 2*d_accel)/v
    #         = 0.1333 + (100 - 6.667)/100 = 0.1333 + 0.9333 = 1.0667s.
    src = """
    G90
    M82
    G1 F6000
    G1 X100 Y0 E1
    """
    r = analyze(src.splitlines(), accel_mm_s2=1500.0)
    v = 100.0
    a = 1500.0
    expected = 2 * (v / a) + (100 - v * v / a) / v
    assert r["estimatedTimeS"] == pytest.approx(expected, rel=1e-6)


def test_toolpath_collected_when_requested():
    src = """
G90
M82
G1 X0 Y0 Z0.2
G1 X10 Y0 E1
G1 X20 Y0
G1 X20 Y10 E2
"""
    r = analyze(src.splitlines(), collect_toolpath=True, accel_mm_s2=None)
    tp = r["toolpath"]
    # 4 motion segments total: initial Z lift (travel), extrusion to X10,
    # travel to X20, extrusion to (X20,Y10). 6 floats per segment.
    assert tp["totalSegments"] == 4
    assert len(tp["extrusion"]) == 12  # 2 segments * 6
    assert len(tp["travel"]) == 12  # 2 segments * 6


